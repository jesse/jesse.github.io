const (
	defaultPostsPerPage = 10
	generatedMarker     = "generated by jesseyoung blog build"
	chromaStyleName     = "monokai"
	responsiveSizes     = "(min-width: 58rem) 54rem, calc(100vw - 2rem)"
	postDateLayout      = "2006-01-02T150405"
)

var (
	slugTimestampPrefix = regexp.MustCompile(`^\d{4}-\d{2}-\d{2}T\d{6}-`)
	slugDatePrefix      = regexp.MustCompile(`^\d{4}-\d{2}-\d{2}-`)
	htmlImageTagPattern = regexp.MustCompile(`(?is)<img([^>]*?)src="([^"]+)"([^>]*)>`)
	markdownImageTarget = regexp.MustCompile(`!\[[^\]]*]\(([^)]+)\)`)
	paragraphPattern    = regexp.MustCompile(`(?is)<p>(.*?)</p>`)
	htmlTagPattern      = regexp.MustCompile(`(?s)<[^>]+>`)
	plainPrePattern     = regexp.MustCompile(`(?s)<pre><code>(.*?)</code></pre>`)
	assetNamePattern = regexp.MustCompile(`[^a-zA-Z0-9]+`)
	protectedDirs       = map[string]struct{}{
		".claude":      {},
		".git":         {},
		"assets":       {},
		"cmd":          {},
		"mathsite":     {},
		"page":         {},
		"posts":        {},
		"templates":    {},
		"waveform-viz": {},
	}
)

type siteBuilder struct {
	root           string
	postsPerPage   int
	stderr         io.Writer
	markdown       goldmark.Markdown
	metadataParser goldmark.Markdown
	chromaCSS      template.CSS
	indexTemplate  *template.Template
	postTemplate   *template.Template
	spellchecker   *misspell.Replacer
}

type Post struct {
	Title         string
	Date          time.Time
	DisplayDate   string
	Tags          []string
	Draft         bool
	Slug          string
	Body          string
	Excerpt       string
	SourcePath    string
	BodyStartLine int
	Content       template.HTML
	AssetPlans    map[string]responsiveImagePlan
}

type responsiveImagePlan struct {
	OriginalPath string
	SourcePath   string
	Variants     []imageVariantPlan
}

type imageVariantPlan struct {
	Width            int
	Height           int
	OutputPath       string
	PostRelativePath string
}

type validationError struct {
	Path    string
	Field   string
	Problem string
}

type indexPageData struct {
	PageTitle    string
	Marker       template.HTML
	Posts        []Post
	NewerPageURL string
	OlderPageURL string
}

type postPageData struct {
	Title       string
	DisplayDate string
	Marker      template.HTML
	Content     template.HTML
	ChromaCSS   template.CSS
}

func (e validationError) Error() string {
	return fmt.Sprintf("%s: %s: %s", e.Path, e.Field, e.Problem)
}

func main() {
	if err := run(os.Args[1:], os.Stderr); err != nil {
		fmt.Fprintln(os.Stderr, err)
		os.Exit(1)
	}
}

func run(args []string, stderr io.Writer) error {
	flags := flag.NewFlagSet("build", flag.ContinueOnError)
	flags.SetOutput(stderr)

	postsPerPage := flags.Int("posts-per-page", defaultPostsPerPage, "number of posts per index page")
	cleanOnly := flags.Bool("clean", false, "remove generated blog output and exit")

	if err := flags.Parse(args); err != nil {
		return err
	}
	if *postsPerPage <= 0 {
		return fmt.Errorf("--posts-per-page must be greater than zero")
	}
	if *cleanOnly {
		return cleanupGeneratedOutput(".")
	}

	return buildSite(".", *postsPerPage, stderr)
}

func buildSite(root string, postsPerPage int, stderr io.Writer) error {
	builder, err := newSiteBuilder(root, postsPerPage, stderr)
	if err != nil {
		return err
	}

	posts, err := builder.loadPosts()
	if err != nil {
		return err
	}
	if err := validatePublishedSlugs(posts); err != nil {
		return err
	}
	if err := builder.spellcheckPosts(posts); err != nil {
		return err
	}

	published := publishedPosts(posts)
	sort.SliceStable(published, func(i, j int) bool {
		return published[i].Date.After(published[j].Date)
	})

	if err := builder.renderPosts(published); err != nil {
		return err
	}
	if err := cleanupGeneratedOutput(root); err != nil {
		return err
	}
	if err := builder.emitResponsiveImages(published); err != nil {
		return err
	}
	if err := builder.writePosts(published); err != nil {
		return err
	}
	if err := builder.writeIndexes(published); err != nil {
		return err
	}

	return nil
}

func newSiteBuilder(root string, postsPerPage int, stderr io.Writer) (*siteBuilder, error) {
	chromaCSS, err := generateChromaCSS()
	if err != nil {
		return nil, err
	}

	indexTemplate, err := template.ParseFiles(filepath.Join(root, "templates", "index.html.tmpl"))
	if err != nil {
		return nil, fmt.Errorf("parse index template: %w", err)
	}
	postTemplate, err := template.ParseFiles(filepath.Join(root, "templates", "post.html.tmpl"))
	if err != nil {
		return nil, fmt.Errorf("parse post template: %w", err)
	}

	spellchecker := misspell.New()
	spellchecker.Compile()

	return &siteBuilder{
		root:         root,
		postsPerPage: postsPerPage,
		stderr:       stderr,
		markdown: goldmark.New(
			goldmark.WithExtensions(
				extension.GFM,
				highlighting.NewHighlighting(
					highlighting.WithStyle(chromaStyleName),
					highlighting.WithGuessLanguage(false),
					highlighting.WithFormatOptions(
						chromahtml.WithClasses(true),
					),
				),
			),
		),
		metadataParser: goldmark.New(
			goldmark.WithExtensions(meta.Meta),
		),
		chromaCSS:     template.CSS(chromaCSS),
		indexTemplate: indexTemplate,
		postTemplate:  postTemplate,
		spellchecker:  spellchecker,
	}, nil
}

func generateChromaCSS() (string, error) {
	style := styles.Get(chromaStyleName)
	if style == nil {
		return "", fmt.Errorf("missing Chroma style %q", chromaStyleName)
	}

	var css bytes.Buffer
	formatter := chromahtml.New(chromahtml.WithClasses(true))
	if err := formatter.WriteCSS(&css, style); err != nil {
		return "", fmt.Errorf("generate Chroma CSS: %w", err)
	}
	return css.String(), nil
}

func (b *siteBuilder) loadPosts() ([]Post, error) {
	paths, err := filepath.Glob(filepath.Join(b.root, "posts", "*.md"))
	if err != nil {
		return nil, fmt.Errorf("discover posts: %w", err)
	}
	sort.Strings(paths)

	posts := make([]Post, 0, len(paths))
	for _, postPath := range paths {
		post, err := b.parsePost(postPath)
		if err != nil {
			return nil, err
		}
		posts = append(posts, post)
	}
	return posts, nil
}

func (b *siteBuilder) parsePost(postPath string) (Post, error) {
	source, err := os.ReadFile(postPath)
	if err != nil {
		return Post{}, fmt.Errorf("read post %s: %w", postPath, err)
	}

	frontMatter, body, bodyStartLine, ok := splitFrontMatter(source)
	if !ok {
		return Post{}, validationError{Path: relativePath(b.root, postPath), Field: "front matter", Problem: "missing YAML front matter block"}
	}
	if strings.TrimSpace(frontMatter) == "" {
		return Post{}, validationError{Path: relativePath(b.root, postPath), Field: "front matter", Problem: "front matter block is empty"}
	}

	metadata, err := b.parseMetadata(source)
	if err != nil {
		return Post{}, fmt.Errorf("%s: parse front matter: %w", relativePath(b.root, postPath), err)
	}

	title, err := parseRequiredString(metadata, "title")
	if err != nil {
		return Post{}, validationError{Path: relativePath(b.root, postPath), Field: "title", Problem: err.Error()}
	}
	dateValue, ok := metadata["date"]
	if !ok {
		return Post{}, validationError{Path: relativePath(b.root, postPath), Field: "date", Problem: "missing required field"}
	}
	date, err := parseDateValue(dateValue)
	if err != nil {
		return Post{}, validationError{Path: relativePath(b.root, postPath), Field: "date", Problem: err.Error()}
	}
	draftValue, ok := metadata["draft"]
	if !ok {
		return Post{}, validationError{Path: relativePath(b.root, postPath), Field: "draft", Problem: "missing required field"}
	}
	draft, ok := draftValue.(bool)
	if !ok {
		return Post{}, validationError{Path: relativePath(b.root, postPath), Field: "draft", Problem: "must be a boolean"}
	}
	tags, err := parseTags(metadata["tags"])
	if err != nil {
		return Post{}, validationError{Path: relativePath(b.root, postPath), Field: "tags", Problem: err.Error()}
	}

	slug := deriveSlug(filepath.Base(postPath))
	if customSlug, ok := metadata["slug"]; ok {
		value, err := parseRequiredString(map[string]interface{}{"slug": customSlug}, "slug")
		if err != nil {
			return Post{}, validationError{Path: relativePath(b.root, postPath), Field: "slug", Problem: err.Error()}
		}
		slug = value
	}
	if err := validateSlug(slug); err != nil {
		return Post{}, validationError{Path: relativePath(b.root, postPath), Field: "slug", Problem: err.Error()}
	}

	post := Post{
		Title:         title,
		Date:          date,
		DisplayDate:   date.Format("January 2, 2006"),
		Tags:          tags,
		Draft:         draft,
		Slug:          slug,
		Body:          body,
		SourcePath:    relativePath(b.root, postPath),
		BodyStartLine: bodyStartLine,
		AssetPlans:    map[string]responsiveImagePlan{},
	}

	if err := b.validateLocalImages(post); err != nil {
		return Post{}, err
	}

	return post, nil
}

func (b *siteBuilder) parseMetadata(source []byte) (map[string]interface{}, error) {
	var rendered bytes.Buffer
	context := parser.NewContext()
	if err := b.metadataParser.Convert(source, &rendered, parser.WithContext(context)); err != nil {
		return nil, err
	}

	raw := meta.Get(context)
	normalized := make(map[string]interface{}, len(raw))
	for key, value := range raw {
		normalized[strings.ToLower(key)] = value
	}
	return normalized, nil
}

func splitFrontMatter(source []byte) (frontMatter string, body string, bodyStartLine int, ok bool) {
	text := strings.ReplaceAll(string(source), "\r\n", "\n")
	lines := strings.Split(text, "\n")
	if len(lines) == 0 || lines[0] != "---" {
		return "", "", 0, false
	}

	for i := 1; i < len(lines); i++ {
		if lines[i] == "---" {
			return strings.Join(lines[1:i], "\n"), strings.Join(lines[i+1:], "\n"), i + 2, true
		}
	}

	return "", "", 0, false
}

func parseRequiredString(metadata map[string]interface{}, key string) (string, error) {
	value, ok := metadata[key]
	if !ok {
		return "", fmt.Errorf("missing required field")
	}
	text, ok := value.(string)
	if !ok {
		return "", fmt.Errorf("must be a string")
	}
	text = strings.TrimSpace(text)
	if text == "" {
		return "", fmt.Errorf("must not be empty")
	}
	return text, nil
}

func parseDateValue(value interface{}) (time.Time, error) {
	text, ok := value.(string)
	if !ok {
		return time.Time{}, fmt.Errorf("must use YYYY-MM-DDTHHMMSS")
	}

	parsed, err := time.Parse(postDateLayout, strings.TrimSpace(text))
	if err != nil {
		return time.Time{}, fmt.Errorf("must use YYYY-MM-DDTHHMMSS")
	}
	return parsed, nil
}

func parseTags(value interface{}) ([]string, error) {
	if value == nil {
		return nil, nil
	}

	switch typed := value.(type) {
	case []string:
		result := make([]string, len(typed))
		copy(result, typed)
		return result, nil
	case []interface{}:
		result := make([]string, 0, len(typed))
		for _, item := range typed {
			text, ok := item.(string)
			if !ok {
				return nil, fmt.Errorf("must be a list of strings")
			}
			result = append(result, text)
		}
		return result, nil
	default:
		return nil, fmt.Errorf("must be a list of strings")
	}
}

func validateSlug(slug string) error {
	if slug == "" {
		return fmt.Errorf("must not be empty")
	}
	if strings.Contains(slug, "/") || strings.Contains(slug, `\`) {
		return fmt.Errorf("must not contain path separators")
	}
	if strings.HasPrefix(slug, ".") {
		return fmt.Errorf("must not start with a dot")
	}
	if _, reserved := protectedDirs[slug]; reserved {
		return fmt.Errorf("is reserved by the site structure")
	}
	return nil
}

func deriveSlug(filename string) string {
	base := strings.TrimSuffix(filename, filepath.Ext(filename))
	base = slugTimestampPrefix.ReplaceAllString(base, "")
	base = slugDatePrefix.ReplaceAllString(base, "")
	return base
}

func validatePublishedSlugs(posts []Post) error {
	seen := map[string]string{}
	for _, post := range posts {
		if post.Draft {
			continue
		}
		if existing, ok := seen[post.Slug]; ok {
			return validationError{
				Path:    post.SourcePath,
				Field:   "slug",
				Problem: fmt.Sprintf("duplicate slug %q already used by %s", post.Slug, existing),
			}
		}
		seen[post.Slug] = post.SourcePath
	}
	return nil
}

func (b *siteBuilder) validateLocalImages(post Post) error {
	targets := extractMarkdownImageTargets(post.Body)
	for _, target := range targets {
		if isRemoteImageRef(target) {
			continue
		}
		if strings.HasPrefix(target, "/") {
			return validationError{Path: post.SourcePath, Field: "image", Problem: fmt.Sprintf("%q must be a relative path", target)}
		}

		resolved, err := resolveLocalImagePath(b.root, post.SourcePath, target)
		if err != nil {
			return validationError{Path: post.SourcePath, Field: "image", Problem: err.Error()}
		}
		if _, _, err := inspectImage(resolved); err != nil {
			return validationError{Path: post.SourcePath, Field: "image", Problem: fmt.Sprintf("%q is not a readable local image: %v", target, err)}
		}
	}

	return nil
}

func extractMarkdownImageTargets(body string) []string {
	matches := markdownImageTarget.FindAllStringSubmatch(body, -1)
	if len(matches) == 0 {
		return nil
	}

	targets := make([]string, 0, len(matches))
	seen := map[string]struct{}{}
	for _, match := range matches {
		target := normalizeMarkdownImageTarget(match[1])
		if target == "" {
			continue
		}
		if _, ok := seen[target]; ok {
			continue
		}
		seen[target] = struct{}{}
		targets = append(targets, target)
	}
	return targets
}

func normalizeMarkdownImageTarget(raw string) string {
	target := strings.TrimSpace(raw)
	if strings.HasPrefix(target, "<") && strings.HasSuffix(target, ">") {
		target = strings.TrimSuffix(strings.TrimPrefix(target, "<"), ">")
	}
	if fields := strings.Fields(target); len(fields) > 0 {
		target = fields[0]
	}
	return strings.TrimSpace(target)
}

func resolveLocalImagePath(root string, sourcePath string, target string) (string, error) {
	if filepath.IsAbs(target) {
		return "", fmt.Errorf("%q must be a relative path", target)
	}

	postDir := filepath.Dir(filepath.Join(root, sourcePath))
	resolved := filepath.Clean(filepath.Join(postDir, filepath.FromSlash(target)))

	rootAbs, err := filepath.Abs(root)
	if err != nil {
		return "", err
	}
	resolvedAbs, err := filepath.Abs(resolved)
	if err != nil {
		return "", err
	}
	relative, err := filepath.Rel(rootAbs, resolvedAbs)
	if err != nil {
		return "", err
	}
	if relative == ".." || strings.HasPrefix(relative, ".."+string(os.PathSeparator)) {
		return "", fmt.Errorf("%q escapes the repository root", target)
	}
	return resolvedAbs, nil
}

func inspectImage(filePath string) (image.Config, string, error) {
	file, err := os.Open(filePath)
	if err != nil {
		return image.Config{}, "", err
	}
	defer file.Close()

	return image.DecodeConfig(file)
}

func (b *siteBuilder) spellcheckPosts(posts []Post) error {
	foundIssue := false
	for _, post := range posts {
		if post.Draft {
			continue
		}

		_, diffs := b.spellchecker.Replace(post.Body)
		for _, diff := range diffs {
			foundIssue = true
			line := post.BodyStartLine + diff.Line - 1
			fmt.Fprintf(b.stderr, "%s:%d:%d found %q a misspelling of %q\n", post.SourcePath, line, diff.Column, diff.Original, diff.Corrected)
		}
	}

	if foundIssue {
		return fmt.Errorf("spellcheck failed")
	}
	return nil
}

func publishedPosts(posts []Post) []Post {
	published := make([]Post, 0, len(posts))
	for _, post := range posts {
		if post.Draft {
			continue
		}
		published = append(published, post)
	}
	return published
}

func (b *siteBuilder) renderPosts(posts []Post) error {
	for i := range posts {
		rendered, err := b.renderMarkdown(posts[i].Body)
		if err != nil {
			return fmt.Errorf("%s: render markdown: %w", posts[i].SourcePath, err)
		}

		rendered = wrapPlainCodeBlocks(rendered)
		rendered, plans, err := b.rewriteImages(posts[i], rendered)
		if err != nil {
			return fmt.Errorf("%s: rewrite images: %w", posts[i].SourcePath, err)
		}

		posts[i].Content = template.HTML(rendered)
		posts[i].Excerpt = extractExcerpt(rendered)
		posts[i].AssetPlans = plans
	}

	return nil
}

func (b *siteBuilder) renderMarkdown(body string) (string, error) {
	var output bytes.Buffer
	if err := b.markdown.Convert([]byte(body), &output); err != nil {
		return "", err
	}
	return output.String(), nil
}

func wrapPlainCodeBlocks(rendered string) string {
	return plainPrePattern.ReplaceAllString(rendered, `<div class="plain-code"><pre><code>$1</code></pre></div>`)
}

func (b *siteBuilder) rewriteImages(post Post, rendered string) (string, map[string]responsiveImagePlan, error) {
	matches := htmlImageTagPattern.FindAllStringSubmatchIndex(rendered, -1)
	if len(matches) == 0 {
		return rendered, map[string]responsiveImagePlan{}, nil
	}

	plans := map[string]responsiveImagePlan{}
	var output strings.Builder
	last := 0

	for _, match := range matches {
		output.WriteString(rendered[last:match[0]])

		before := rendered[match[2]:match[3]]
		src := html.UnescapeString(rendered[match[4]:match[5]])
		after := rendered[match[6]:match[7]]
		attrs := strings.TrimSpace(strings.Join([]string{strings.TrimSpace(before), strings.TrimSpace(after)}, " "))

		if isRemoteImageRef(src) {
			output.WriteString(buildImageTag(attrs, src, "", "", true))
		} else {
			plan, ok := plans[src]
			if !ok {
				var err error
				plan, err = b.planResponsiveImage(post, src)
				if err != nil {
					return "", nil, err
				}
				plans[src] = plan
			}
			output.WriteString(buildImageTag(attrs, plan.LargestPostRelativePath(), buildSrcset(plan), responsiveSizes, true))
		}

		last = match[1]
	}

	output.WriteString(rendered[last:])
	return output.String(), plans, nil
}

func buildImageTag(attrs string, src string, srcset string, sizes string, lazy bool) string {
	var builder strings.Builder
	builder.WriteString("<img")
	if attrs != "" {
		builder.WriteByte(' ')
		builder.WriteString(strings.TrimSpace(attrs))
	}
	builder.WriteString(` src="`)
	builder.WriteString(template.HTMLEscapeString(src))
	builder.WriteString(`"`)
	if srcset != "" {
		builder.WriteString(` srcset="`)
		builder.WriteString(template.HTMLEscapeString(srcset))
		builder.WriteString(`" sizes="`)
		builder.WriteString(template.HTMLEscapeString(sizes))
		builder.WriteString(`"`)
	}
	if lazy {
		builder.WriteString(` loading="lazy"`)
	}
	builder.WriteString(">")
	return builder.String()
}

func (b *siteBuilder) planResponsiveImage(post Post, originalSrc string) (responsiveImagePlan, error) {
	resolved, err := resolveLocalImagePath(b.root, post.SourcePath, originalSrc)
	if err != nil {
		return responsiveImagePlan{}, err
	}
	config, format, err := inspectImage(resolved)
	if err != nil {
		return responsiveImagePlan{}, err
	}

	ext := strings.ToLower(filepath.Ext(resolved))
	if ext == "" {
		ext = "." + strings.ToLower(format)
	}

	base := sanitizeAssetBaseName(strings.TrimSuffix(filepath.Base(resolved), filepath.Ext(resolved)))
	if base == "" {
		base = "image"
	}

	widths := responsiveWidths(config.Width)
	variants := make([]imageVariantPlan, 0, len(widths))
	for _, width := range widths {
		height := int(math.Round(float64(config.Height) * (float64(width) / float64(config.Width))))
		outputPath := path.Join("assets", post.Slug, fmt.Sprintf("%s-%dw%s", base, width, ext))
		variants = append(variants, imageVariantPlan{
			Width:            width,
			Height:           height,
			OutputPath:       outputPath,
			PostRelativePath: path.Join("..", outputPath),
		})
	}

	return responsiveImagePlan{
		OriginalPath: originalSrc,
		SourcePath:   resolved,
		Variants:     variants,
	}, nil
}

func sanitizeAssetBaseName(name string) string {
	name = strings.ToLower(strings.TrimSpace(name))
	name = assetNamePattern.ReplaceAllString(name, "-")
	return strings.Trim(name, "-")
}

func responsiveWidths(sourceWidth int) []int {
	targets := []int{480, 768, 1200, 1600}
	widths := make([]int, 0, len(targets)+1)
	for _, width := range targets {
		if width <= sourceWidth {
			widths = append(widths, width)
		}
	}
	if len(widths) == 0 || widths[len(widths)-1] != sourceWidth {
		widths = append(widths, sourceWidth)
	}
	return widths
}

func buildSrcset(plan responsiveImagePlan) string {
	items := make([]string, 0, len(plan.Variants))
	for _, variant := range plan.Variants {
		items = append(items, fmt.Sprintf("%s %dw", variant.PostRelativePath, variant.Width))
	}
	return strings.Join(items, ", ")
}

func (plan responsiveImagePlan) LargestPostRelativePath() string {
	if len(plan.Variants) == 0 {
		return ""
	}
	return plan.Variants[len(plan.Variants)-1].PostRelativePath
}

func extractExcerpt(rendered string) string {
	matches := paragraphPattern.FindAllStringSubmatch(rendered, -1)
	for _, match := range matches {
		text := html.UnescapeString(stripHTML(match[1]))
		text = collapseWhitespace(text)
		if text == "" {
			continue
		}
		return truncateText(text, 200)
	}
	return ""
}

func stripHTML(input string) string {
	return htmlTagPattern.ReplaceAllString(input, "")
}

func collapseWhitespace(input string) string {
	return strings.Join(strings.Fields(input), " ")
}

func truncateText(input string, limit int) string {
	runes := []rune(input)
	if len(runes) <= limit {
		return input
	}

	cut := string(runes[:limit])
	if index := strings.LastIndex(cut, " "); index > limit/2 {
		cut = cut[:index]
	}
	return strings.TrimSpace(cut) + "..."
}

func (b *siteBuilder) emitResponsiveImages(posts []Post) error {
	for _, post := range posts {
		keys := make([]string, 0, len(post.AssetPlans))
		for key := range post.AssetPlans {
			keys = append(keys, key)
		}
		sort.Strings(keys)

		for _, key := range keys {
			if err := emitResponsiveImagePlan(filepath.Join(b.root), post.AssetPlans[key]); err != nil {
				return fmt.Errorf("%s: generate responsive images for %q: %w", post.SourcePath, key, err)
			}
		}
	}
	return nil
}

func emitResponsiveImagePlan(root string, plan responsiveImagePlan) error {
	file, err := os.Open(plan.SourcePath)
	if err != nil {
		return err
	}
	defer file.Close()

	sourceImage, format, err := image.Decode(file)
	if err != nil {
		return err
	}

	for _, variant := range plan.Variants {
		outputPath := filepath.Join(root, filepath.FromSlash(variant.OutputPath))
		if err := os.MkdirAll(filepath.Dir(outputPath), 0o755); err != nil {
			return err
		}

		scaled := resizeImage(sourceImage, variant.Width, variant.Height)
		if err := encodeImage(outputPath, scaled, format); err != nil {
			return err
		}
	}

	return nil
}

func resizeImage(source image.Image, width int, height int) image.Image {
	if source.Bounds().Dx() == width && source.Bounds().Dy() == height {
		return source
	}

	destination := image.NewRGBA(image.Rect(0, 0, width, height))
	xdraw.CatmullRom.Scale(destination, destination.Bounds(), source, source.Bounds(), xdraw.Over, nil)
	return destination
}

func encodeImage(outputPath string, img image.Image, detectedFormat string) error {
	file, err := os.Create(outputPath)
	if err != nil {
		return err
	}
	defer file.Close()

	ext := strings.ToLower(filepath.Ext(outputPath))
	switch ext {
	case ".jpg", ".jpeg":
		return jpeg.Encode(file, img, &jpeg.Options{Quality: 85})
	case ".gif":
		return gif.Encode(file, img, nil)
	case ".png":
		return png.Encode(file, img)
	default:
		switch strings.ToLower(detectedFormat) {
		case "jpeg":
			return jpeg.Encode(file, img, &jpeg.Options{Quality: 85})
		case "gif":
			return gif.Encode(file, img, nil)
		default:
			return png.Encode(file, img)
		}
	}
}

func (b *siteBuilder) writePosts(posts []Post) error {
	for _, post := range posts {
		outputPath := filepath.Join(b.root, post.Slug, "index.html")
		data := postPageData{
			Title:       post.Title,
			DisplayDate: post.DisplayDate,
			Marker:      template.HTML("<!-- " + generatedMarker + " -->"),
			Content:     post.Content,
			ChromaCSS:   b.chromaCSS,
		}
		if err := writeTemplateFile(outputPath, b.postTemplate, data); err != nil {
			return fmt.Errorf("write post %s: %w", outputPath, err)
		}
	}
	return nil
}

func (b *siteBuilder) writeIndexes(posts []Post) error {
	pages := paginatePosts(posts, b.postsPerPage)
	totalPages := len(pages)

	for i, pagePosts := range pages {
		pageNumber := i + 1
		outputPath := filepath.Join(b.root, "index.html")
		if pageNumber > 1 {
			outputPath = filepath.Join(b.root, "page", strconv.Itoa(pageNumber), "index.html")
		}

		title := "Jesse Young"
		if pageNumber > 1 {
			title = fmt.Sprintf("Jesse Young | Page %d", pageNumber)
		}

		data := indexPageData{
			PageTitle: title,
			Marker:    template.HTML("<!-- " + generatedMarker + " -->"),
			Posts:     pagePosts,
		}
		if pageNumber > 1 {
			data.NewerPageURL = pageURL(pageNumber - 1)
		}
		if pageNumber < totalPages {
			data.OlderPageURL = pageURL(pageNumber + 1)
		}

		if err := writeTemplateFile(outputPath, b.indexTemplate, data); err != nil {
			return fmt.Errorf("write index %s: %w", outputPath, err)
		}
	}

	return nil
}

func writeTemplateFile(outputPath string, tmpl *template.Template, data interface{}) error {
	if err := os.MkdirAll(filepath.Dir(outputPath), 0o755); err != nil {
		return err
	}

	var rendered bytes.Buffer
	if err := tmpl.Execute(&rendered, data); err != nil {
		return err
	}
	return os.WriteFile(outputPath, rendered.Bytes(), 0o644)
}

func paginatePosts(posts []Post, postsPerPage int) [][]Post {
	if len(posts) == 0 {
		return [][]Post{{}}
	}

	pages := make([][]Post, 0, (len(posts)+postsPerPage-1)/postsPerPage)
	for start := 0; start < len(posts); start += postsPerPage {
		end := start + postsPerPage
		if end > len(posts) {
			end = len(posts)
		}
		pages = append(pages, posts[start:end])
	}
	return pages
}

func pageURL(pageNumber int) string {
	if pageNumber <= 1 {
		return "/"
	}
	return fmt.Sprintf("/page/%d/", pageNumber)
}

func cleanupGeneratedOutput(root string) error {
	if err := removeGeneratedIndex(root); err != nil {
		return err
	}
	if err := os.RemoveAll(filepath.Join(root, "page")); err != nil {
		return err
	}
	if err := os.RemoveAll(filepath.Join(root, "assets")); err != nil {
		return err
	}

	entries, err := os.ReadDir(root)
	if err != nil {
		return err
	}
	for _, entry := range entries {
		if !entry.IsDir() {
			continue
		}
		if _, protected := protectedDirs[entry.Name()]; protected {
			continue
		}

		indexPath := filepath.Join(root, entry.Name(), "index.html")
		matches, err := fileContains(indexPath, generatedMarker)
		if err != nil {
			return err
		}
		if matches {
			if err := os.RemoveAll(filepath.Join(root, entry.Name())); err != nil {
				return err
			}
		}
	}
	return nil
}

func removeGeneratedIndex(root string) error {
	indexPath := filepath.Join(root, "index.html")
	matches, err := fileContains(indexPath, generatedMarker)
	if err != nil {
		return err
	}
	if matches {
		return os.Remove(indexPath)
	}
	return nil
}

func fileContains(filePath string, target string) (bool, error) {
	content, err := os.ReadFile(filePath)
	if err != nil {
		if os.IsNotExist(err) {
			return false, nil
		}
		return false, err
	}
	return strings.Contains(string(content), target), nil
}

func isRemoteImageRef(target string) bool {
	switch {
	case strings.HasPrefix(target, "http://"):
		return true
	case strings.HasPrefix(target, "https://"):
		return true
	case strings.HasPrefix(target, "//"):
		return true
	case strings.HasPrefix(target, "data:"):
		return true
	default:
		return false
	}
}

func relativePath(root string, filePath string) string {
	relative, err := filepath.Rel(root, filePath)
	if err != nil {
		return filePath
	}
	return relative
}
